    @Override
    public Estado encontrarId(int id) {
        Estado response = null;
        IConexion conexionDB = ConexionFactory.getConexion("MYSQL");
        if (conexionDB.conectar() == 1) {
            try {
                String sql = "SELECT "
                        + "idEstado, "
                        + "estado "
                        + "FROM Estado "
                        + "WHERE idEstado=?";
                PreparedStatement ps = conexionDB.getConexion().prepareStatement(sql);
                ps.setInt(1, id);
                ResultSet rs;
                rs = ps.executeQuery();
                if (rs.next()) {
                    response = new Estado();
                    response.setIdEstado(rs.getInt(1));
                    response.setEstado(rs.getString(2));
                } else {
                    Logg.error("No se encontro ningun registro");
                }
            } catch (SQLException ex) {
                Logg.error("Comunicación fallida con la base de datos");
            } finally {
                conexionDB.desconectar();
            }
        } else {
            Logg.error("Conexión fallida con la base de datos");
        }
        return response;
    }

    public List<Municipio> getLstMunicipios(int id) {
        List<Municipio> response = new ArrayList<>();
        IConexion conexionDB = ConexionFactory.getConexion("MYSQL");
        if (conexionDB.conectar() == 1) {
            try {
                String sql = "SELECT "
                        + "idMunicipio, "
                        + "idEstado, "
                        + "municipio "
                        + "FROM Municipio "
                        + "WHERE idEstado=?";
                PreparedStatement ps = conexionDB.getConexion().prepareStatement(sql);
                ps.setInt(1, id);
                ResultSet rs;
                rs = ps.executeQuery();
                if (rs.next()) {
                    Municipio mun = new Municipio();
                    mun.setIdMunicipio(rs.getInt(1));
                    mun.setIdEstado(rs.getInt(2));
                    mun.setMunicipio(rs.getString(3));
                    response.add(mun);
                } else {
                    Logg.error("No se encontro ningun registro");
                }
            } catch (SQLException ex) {
                Logg.error("Comunicación fallida con la base de datos");
            } finally {
                conexionDB.desconectar();
            }
        } else {
            Logg.error("Conexión fallida con la base de datos");
        }
        return response;
    }
